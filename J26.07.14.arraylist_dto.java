package g_arraylist;

public class Java_03_dto {
	String no,name,dapart,rank;
	int age;
	public Java_03_dto() {}
	
	public Java_03_dto(String no, String name, String dapart, String rank, int age) {
		this.no = no;
		this.name = name;
		this.dapart = dapart;
		this.rank = rank;
		this.age = age;
	}

	
	
	
	public String getNo() {
		return no;
	}

	public void setNo(String no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDapart() {
		return dapart;
	}

	public void setDapart(String dapart) {
		this.dapart = dapart;
	}

	public String getRank() {
		return rank;
	}

	public void setRank(String rank) {
		this.rank = rank;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
